private void getdata(){
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);
    try{
        String query="select * from payment";
        ResultSet rs=sd.getdata(query);
        while(rs.next()){
            model.addRow(new Object[]{rs.getString("t_id"),rs.getString("name"),rs.getString("yr"),rs.getString("month"),rs.getString("amount")});
        }
    }
    catch(Exception e){
        
    }
}
